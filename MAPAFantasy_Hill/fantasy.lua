function map()
	txd = engineLoadTXD ( "fantasy.txd" )
		engineImportTXD ( txd, 18002 )
		engineImportTXD ( txd, 18003 )
		engineImportTXD ( txd, 18004 )
		engineImportTXD ( txd, 18005 )
		engineImportTXD ( txd, 18006 )
	col = engineLoadCOL ( "fantasy1.col" )
	col1 = engineLoadCOL ( "fantasy2.col" )
	col2 = engineLoadCOL ( "fantasy3.col" )
	col3 = engineLoadCOL ( "fantasy4.col" )
	col4 = engineLoadCOL ( "fantasy5.col" )
	dff = engineLoadDFF ( "fantasy1.dff", 0 )
	dff1 = engineLoadDFF ( "fantasy2.dff", 0 )
	dff2 = engineLoadDFF ( "fantasy3.dff", 0 )
	dff3 = engineLoadDFF ( "fantasy4.dff", 0 )
	dff4 = engineLoadDFF ( "fantasy5.dff", 0 )
	engineReplaceCOL ( col, 18002 )
	engineReplaceCOL ( col1, 18003 )
	engineReplaceCOL ( col2, 18004 )
	engineReplaceCOL ( col3, 18005 )
	engineReplaceCOL ( col4, 18006 )
	engineReplaceModel ( dff, 18002 )
	engineReplaceModel ( dff1, 18003 )
	engineReplaceModel ( dff2, 18004 )
	engineReplaceModel ( dff3, 18005 )
	engineReplaceModel ( dff4, 18006 )
	engineSetModelLODDistance(18002, 2000)
	engineSetModelLODDistance(18003, 2000)
	engineSetModelLODDistance(18004, 2000)
	engineSetModelLODDistance(18005, 2000)
	engineSetModelLODDistance(18006, 2000)
end

function chat()
		outputChatBox('Fantasy Hill Race', player, 0, 255, 0)
end

setTimer ( map, 1000, 1)
addCommandHandler("reloadmap",map)

addEventHandler("onClientResourceStop", getResourceRootElement(getThisResource()),
	function()
		engineRestoreCOL(18002)
		engineRestoreCOL(18003)
		engineRestoreCOL(18004)
		engineRestoreCOL(18005)
		engineRestoreCOL(18006)
		engineRestoreModel(18002)
		engineRestoreModel(18003)
		engineRestoreModel(18004)
		engineRestoreModel(18005)
		engineRestoreModel(18006)
		destroyElement(dff)
		destroyElement(dff1)
		destroyElement(dff2)
		destroyElement(dff3)
		destroyElement(dff4)
		destroyElement(col)
		destroyElement(col1)
		destroyElement(col2)
		destroyElement(col3)
		destroyElement(col4)
		destroyElement(txd)
	end
)